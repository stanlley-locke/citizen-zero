import qrcode
from io import BytesIO

class QRGenerator:
    @staticmethod
    def generate(data):
        qr = qrcode.QRCode(version=1, box_size=10, border=5)
        qr.add_data(data)
        qr.make(fit=True)
        img = qr.make_image(fill_color="black", back_color="white")
        
        buffer = BytesIO()
        img.save(buffer)
        return buffer.getvalue()
